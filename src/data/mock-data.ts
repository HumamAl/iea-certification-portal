export const getCertificantById = (id: string): Certificant | undefined =>
  certificants.find((c) => c.id === id);

export const getProgramById = (id: string): CertificationProgram | undefined =>
  certificationPrograms.find((p) => p.id === id);

export const getCredentialById = (id: string): CredentialRecord | undefined =>
  credentialRecords.find((r) => r.id === id);

export const getCredentialsByCertificant = (certificantId: string): CredentialRecord[] =>
  credentialRecords.filter((r) => r.certificantId === certificantId);

export const getCECreditsByCredential = (credentialRecordId: string): CECredit[] =>
  ceCredits.filter((c) => c.credentialRecordId === credentialRecordId);

export const getExamAttemptsByCandidate = (certificantId: string): ExamAttempt[] =>
  examAttempts.filter((e) => e.certificantId === certificantId);

export const getApplicationsByProgram = (programId: string): Application[] =>
  applications.filter((a) => a.programId === programId);

// Program lookup map for O(1) access
export const programLookup: Record<string, CertificationProgram> = Object.fromEntries(
  certificationPrograms.map((p) => [p.id, p])
);

export const certificantLookup: Record<string, Certificant> = Object.fromEntries(
  certificants.map((c) => [c.id, c])
);
